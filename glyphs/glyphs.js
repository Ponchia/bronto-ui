/**
 * Glyph names whose name OR a search alias contains `query` (case-insensitive),
 * sorted. `findGlyphs('')` returns every name. For discovery — a CMS field, an
 * icon picker, an LLM resolving an intent word to a real glyph.
 *
 * @param {string} query
 * @returns {GlyphName[]}
 */
export function findGlyphs(query) {
  const q = String(query ?? '')
    .trim()
    .toLowerCase();
  if (!q) return [...GLYPH_NAMES];
  return GLYPH_NAMES.filter(
    (name) =>
      name.toLowerCase().includes(q) ||
      (GLYPH_TAGS[name] ?? []).some((t) => t.toLowerCase().includes(q)),
  );
}

const TONE = { '#': 'hot', '*': 'accent' };

/** The raw bitmap rows for a glyph, or `undefined` if the name is unknown. */
export function glyph(name) {
  return GLYPHS[name];
}

/** GLYPH_SIZE² cell descriptors (row-major), or `[]` if the name is unknown. */
export function glyphCells(name) {
  const rows = GLYPHS[name];
  if (!rows) return [];
  const cells = [];
  for (const row of rows) {
    for (const ch of row) {
      if (ch === '.') cells.push({ on: false });
      else cells.push({ on: true, tone: TONE[ch] });
    }
  }
  return cells;
}

/**
 * The CSS `mask-image` `url()` for a glyph's lit cells — the value of
 * `--icon-mask` on a `.ui-icon` element (the one-node, currentColor,
 * scales-with-text icon path). `''` if the name is unknown. Single-tone:
 * accent `*` cells become opaque mask regions, like hot `#`.
 *
 * @param {GlyphNameInput} name
 * @returns {string}
 */
export function glyphMask(name) {
  const rows = GLYPHS[name];
  return rows ? maskUrl(rows) : '';
}

const CELL = 'ui-dotmatrix__cell';

function cellClass(cell) {
  if (!cell.on) return CELL;
  if (cell.tone === 'hot') return `${CELL} ${CELL}--hot`;
  if (cell.tone === 'accent') return `${CELL} ${CELL}--accent`;
  return CELL;
}

function esc(s) {
  return String(s)
    .replace(/&/g, '&amp;')
    .replace(/</g, '&lt;')
    .replace(/>/g, '&gt;')
    .replace(/"/g, '&quot;');
}

// `dot`, `gap`, and `size` land in an inline-CSS context (`style="…"`), where
// HTML-escaping a `"` stops attribute breakout but a `;` would still open a
// second CSS declaration (overlay/clickjacking, selector exfil). So restrict
// them to length/calc syntax — digits, units, %, whitespace and `()+-*/.,` for
// calc()/clamp()/var() — and drop anything else rather than emit it.
function cssLen(v) {
  return /^[\w.%+\-*/()\s,]+$/.test(v) ? v : '';
}

/**
 * Build a CSS `mask-image` `url()` of an inline SVG for a glyph's lit cells —
 * the basis of the one-node `render: 'mask'` path. Horizontal runs are merged
 * into single `<rect>`s (fewer nodes, no seams). The SVG is alpha-masked, so
 * the element's `currentColor` background shows through only the lit cells —
 * one DOM node instead of GLYPH_SIZE² (256), for an icon at any size/colour.
 */
function maskUrl(rows) {
  let rects = '';
  rows.forEach((row, y) => {
    let run = 0;
    for (let x = 0; x <= GLYPH_SIZE; x++) {
      const on = x < GLYPH_SIZE && row[x] !== '.';
      if (on) run++;
      else if (run) {
        rects += `<rect x='${x - run}' y='${y}' width='${run}' height='1'/>`;
        run = 0;
      }
    }
  });
  // The url() lands unquoted inside an HTML `style="…"` attribute, so it must
  // carry no spaces or quotes of its own — encode them (and `<>#`) so it stays
  // a single CSS url-token and can't break out of the attribute.
  const svg = `<svg xmlns='http://www.w3.org/2000/svg' viewBox='0 0 ${GLYPH_SIZE} ${GLYPH_SIZE}'>${rects}</svg>`;
  const enc = svg
    .replace(/%/g, '%25')
    .replace(/</g, '%3C')
    .replace(/>/g, '%3E')
    .replace(/#/g, '%23')
    .replace(/'/g, '%27')
    .replace(/ /g, '%20');
  return `url(data:image/svg+xml,${enc})`;
}

/**
 * A full `.ui-dotmatrix` HTML string for a glyph (`''` if the name is
 * unknown). Decorative by default (`aria-hidden`); pass `label` to expose it
 * as `role="img"`. Pins `--dotmatrix-cols` to GLYPH_SIZE so the square layout
 * holds regardless of the 12-col default; `dot`/`gap` set `--dotmatrix-dot` /
 * `--dotmatrix-gap` (sanitized to a CSS length/calc allowlist; a malformed
 * value is dropped). `grid: false` drops the unlit panel dots (glyph-only).
 *
 * `solid: true` renders the lit cells as square, gapless pixels (a filled
 * silhouette) instead of separated dots — the legible mode at small/inline
 * sizes (~16–24px) where the dot look fragments. It implies glyph-only.
 *
 * `anim` opts into a decorative animation (disabled under reduced-motion;
 * the static frame + `label` still carry the meaning): `'reveal'` powers the
 * cells on in a scan, `'pulse'` makes the glyph breathe.
 *
 * `render: 'mask'` returns a single `.ui-icon` element masked by the glyph
 * bitmap (one DOM node, not GLYPH_SIZE²) — the icon-at-scale path: it sizes to
 * `size` (or `--icon-size` / `1em`) and inherits `currentColor`. The
 * cell-mode options (grid/solid/anim/dot/gap) don't apply; `label` does.
 * Mask mode is single-tone: accent `*` cells (used by `spark`) render
 * identically to hot `#` cells — both become opaque mask regions.
 * Needs `@ponchia/ui/css` (the `.ui-icon` rule).
 */
export function renderGlyph(name, options = {}) {
  const rows = GLYPHS[name];
  if (!rows) return '';
  const { grid = true, solid = false, anim, label, dot, gap, render, size } = options;

  if (render === 'mask') return renderMaskGlyph(rows, { label, size });

  const cells = glyphCells(name);
  const style = dotmatrixStyle({ solid, dot, gap });
  const cls = dotmatrixClass(anim);
  const stagger = anim === 'reveal';
  const showPanel = grid && !solid;
  const inner = cells
    .map((cell, index) => renderGlyphCell(cell, index, { showPanel, stagger }))
    .join('');

  // A `<span>` (not `<div>`): it's phrasing content, so the glyph is valid
  // inline and inside a `<button>` (its content model is phrasing-only) — the
  // inline-icon use. `.ui-dotmatrix`'s `display: grid` works on a span.
  return `<span class="${cls}" style="${style.join(';')}" ${a11yAttrs(label)}>${inner}</span>`;
}

function renderMaskGlyph(rows, { label, size }) {
  // One-node icon: a single `.ui-icon` span masked by the glyph's bitmap, so
  // it scales to any font-size and inherits `currentColor` — for icon-at-scale
  // (e.g. one in every table row) where the GLYPH_SIZE²-cell path is too heavy.
  const iconSize = size && cssLen(size) ? `--icon-size:${cssLen(size)};` : '';
  return `<span class="ui-icon" style="${iconSize}--icon-mask:${maskUrl(rows)}" ${a11yAttrs(
    label,
  )}></span>`;
}

function dotmatrixStyle({ solid, dot, gap }) {
  const style = [`--dotmatrix-cols:${GLYPH_SIZE}`];
  const dotLen = dot && cssLen(dot);
  const gapLen = gap && cssLen(gap);
  // Default the dot track to an intrinsic icon scale when the author set no
  // (valid) `--dotmatrix-dot`. Without it the CSS grid falls back to
  // `minmax(0, 1fr)` and the 16×16 matrix balloons to fill its container
  // (full-bleed, ~1250px) — the string API would then render an icon-intent call
  // very differently from the DOM `initDotGlyph` path, which already defaults to
  // 0.08em. Mirror that default here so both paths render the same icon.
  style.push(`--dotmatrix-dot:${dotLen || '0.08em'}`);
  // Solid mode fuses the dots into a crisp pixel glyph: square cells, no gap.
  if (solid) style.push('--dotmatrix-dot-radius:0', '--dotmatrix-gap:0');
  else if (gapLen) style.push(`--dotmatrix-gap:${gapLen}`);
  return style;
}

function dotmatrixClass(anim) {
  if (anim === 'reveal') return 'ui-dotmatrix ui-dotmatrix--reveal';
  if (anim === 'pulse') return 'ui-dotmatrix ui-dotmatrix--pulse';
  return 'ui-dotmatrix';
}

function renderGlyphCell(cell, index, { showPanel, stagger }) {
  const cl = cell.on ? cellClass(cell) : 'ui-dotmatrix__cell';
  const cellStyle = [];
  // Off cells keep the cell class (so they hold their grid track and 1:1
  // aspect-ratio); `grid: false` (implied by `solid`) only drops their lit
  // background, for the glyph-only look, without collapsing all-off rows.
  if (!cell.on && !showPanel) cellStyle.push('background:transparent');
  // `reveal` staggers each cell by its row-major index via `--i`.
  if (stagger) cellStyle.push(`--i:${index}`);
  const style = cellStyle.length ? ` style="${cellStyle.join(';')}"` : '';
  return `<span class="${cl}"${style}></span>`;
}

function a11yAttrs(label) {
  return label ? `role="img" aria-label="${esc(label)}"` : 'aria-hidden="true"';
}

// Character → glyph name for renderReadout. Punctuation reuses the readout face
// (digit-0..9 + colon/comma/period/percent) plus the existing minus/plus.
const READOUT_CHARS = {
  0: 'digit-0',
  1: 'digit-1',
  2: 'digit-2',
  3: 'digit-3',
  4: 'digit-4',
  5: 'digit-5',
  6: 'digit-6',
  7: 'digit-7',
  8: 'digit-8',
  9: 'digit-9',
  ':': 'colon',
  ',': 'comma',
  '.': 'period',
  '%': 'percent',
  '-': 'minus',
  '+': 'plus',
};

/**
 * Compose a string of digits/punctuation into a row of dot-matrix glyphs — the
 * Nothing-style big numeric readout for a hero KPI, clock, countdown, or
 * percentage. Every character is a decorative glyph; the readout's *value* is
 * carried by the accessible name, so a `label` is STRONGLY recommended (it
 * defaults to the raw `text` when omitted — fine for "12:48", but pass a
 * fuller label like "Revenue: 12,480 USD" when the digits alone are ambiguous).
 *
 * Recognised characters: `0-9 : , . % - +` and space (a blank advance).
 * Anything else is skipped. The per-glyph options (`solid`, `render: 'mask'`,
 * `dot`, `gap`, `anim`) pass straight through to renderGlyph for each character,
 * so a mask-mode readout is one node per glyph (lightest) and a cell-mode one is
 * the full dot-display look. Returns `''` for empty input. Needs
 * `@ponchia/ui/css` (the `.ui-readout` + `.ui-dotmatrix`/`.ui-icon` rules).
 *
 * @param {string} text
 * @param {RenderReadoutOptions} [options]
 * @returns {string}
 */
export function renderReadout(text, options = {}) {
  const str = String(text ?? '');
  if (!str) return '';
  const { label, gap, ...glyphOpts } = options;
  const a11y = `role="img" aria-label="${esc(label ?? str)}"`;
  // The glyphs inside are always decorative — the wrapper carries the name.
  const inner = [...str]
    .map((ch) => {
      if (ch === ' ') return '<span class="ui-readout__spacer" aria-hidden="true"></span>';
      const name = READOUT_CHARS[ch];
      if (!name) return '';
      return renderGlyph(name, glyphOpts);
    })
    .join('');
  const style = gap && cssLen(gap) ? ` style="--readout-gap:${cssLen(gap)}"` : '';
  return `<span class="ui-readout"${style} ${a11y}>${inner}</span>`;
}
