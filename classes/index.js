/** classnames-style joiner: skips falsy, flattens nested arrays of any depth. */
export function cx(...parts) {
  const out = [];
  for (const p of parts.flat(Infinity)) if (p) out.push(p);
  return out.join(' ');
}

const j = (...p) => p.filter(Boolean).join(' ');

